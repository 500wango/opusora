QString TranslationService::language() const
{
    return m_language;
}

int TranslationService::revision() const
{
    return m_revision;
}

QString TranslationService::t(const QString& key) const
{
    const auto dictionary = activeDictionary();
    const auto translated = dictionary.constFind(key);
    if (translated != dictionary.constEnd()) {
        return translated.value();
    }

    const auto fallback = m_en.constFind(key);
    return fallback == m_en.constEnd() ? key : fallback.value();
}

void TranslationService::setLanguage(const QString& language)
{
    const QString normalized = language == QStringLiteral("zh-CN") ? QStringLiteral("zh-CN") : QStringLiteral("en-US");
    if (m_language == normalized) {
        return;
    }

    m_language = normalized;
    ++m_revision;
    emit languageChanged();
}

QHash<QString, QString> TranslationService::activeDictionary() const
{
    return m_language == QStringLiteral("zh-CN") ? m_zh : m_en;
}

